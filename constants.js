// variable are defined using let keyword, their values can be chnaged anytime in the code
let fname = "jack";
fname = "mike";
console.log(fname);

// constants are defined using const keyword, once defined, their value cannot be changed in the code
const phoneno = 12346789;
//phoneno = 22;  constants cannot be reassigned
console.log(phoneno);

const addr = "jaya nagar";
console.log(addr)
//addr = 'ahmedabad' reassignment not possible for constants 
console.log(addr)